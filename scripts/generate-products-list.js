import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const PRODUCTS_DIR = path.join(__dirname, '..', 'public', 'assets', 'products');
const OUTPUT_FILE = path.join(__dirname, '..', 'src', 'data', 'dynamicProductsList.js');

function formatProductName(fileName) {
  let name = fileName.substring(0, fileName.lastIndexOf('.'));
  name = name.replace(/(?:[_\s]+0?\d+|\s+\d+)$/i, '');
  name = name.replace(/_/g, ' ');
  name = name.trim();
  name = name.split(/\s+/)
    .map(word => {
      if (!word) return '';
      const upperWord = word.toUpperCase();
      const techAcronyms = ['GC', 'GCHS', 'GCMS', 'CRM', 'CRMS', 'UV', 'HPLC', 'SS', 'LC', 'PTFE', 'PH'];
      if (techAcronyms.includes(upperWord)) {
        return upperWord === 'PH' ? 'pH' : upperWord;
      }
      return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
    })
    .join(' ');
  return name;
}

function scanDirectory() {
  const dynamicProductsMap = {};

  if (!fs.existsSync(PRODUCTS_DIR)) {
    console.error(`Directory not found: ${PRODUCTS_DIR}`);
    process.exit(1);
  }

  const folders = fs.readdirSync(PRODUCTS_DIR);

  for (const folder of folders) {
    const folderPath = path.join(PRODUCTS_DIR, folder);
    const stat = fs.statSync(folderPath);

    if (!stat.isDirectory() || folder === 'Analytical Instruments') {
      continue;
    }

    let categoryName = '';
    if (folder === 'Laboratory Instruments') {
      categoryName = 'Laboratory Instruments';
    } else if (folder === 'Consumables') {
      categoryName = 'Consumables';
    } else if (folder === 'Calibration_Ref Standards_Sample Prep') {
      categoryName = 'Calibration / Reference Standards / Sample Preparation';
    } else if (folder === 'Support & Utility Supplies') {
      categoryName = 'Support & Utility Systems Supplies';
    } else if (folder === 'Accessories') {
      categoryName = 'Accessories';
    } else {
      continue; // Skip unrecognized folders
    }

    const files = fs.readdirSync(folderPath);
    for (const file of files) {
      const filePath = path.join(folderPath, file);
      const fileStat = fs.statSync(filePath);

      if (fileStat.isDirectory()) continue;

      const ext = path.extname(file).toLowerCase();
      if (!['.png', '.jpg', '.jpeg', '.jfif', '.webp'].includes(ext)) {
        continue;
      }

      const productName = formatProductName(file);
      const productKey = `${folder.toLowerCase().replace(/[^a-z0-9]+/g, '-')}-${productName.toLowerCase().replace(/[^a-z0-9]+/g, '-')}`;

      // URL path that matches the public folder structure
      const imageUrl = `/assets/products/${folder}/${file}`;

      if (!dynamicProductsMap[productKey]) {
        dynamicProductsMap[productKey] = {
          id: productKey,
          name: productName,
          category: categoryName,
          images: [],
        };
      }

      dynamicProductsMap[productKey].images.push(imageUrl);
    }
  }

  // Sort images and format data
  const products = Object.values(dynamicProductsMap).map(product => {
    product.images.sort((a, b) => a.localeCompare(b));
    return product;
  });

  const content = `// This file is auto-generated by scripts/generate-products-list.js
// Do not edit this file directly.

export const rawDynamicProducts = ${JSON.stringify(products, null, 2)};
`;

  // Ensure target folder exists
  const outputDir = path.dirname(OUTPUT_FILE);
  if (!fs.existsSync(outputDir)) {
    fs.mkdirSync(outputDir, { recursive: true });
  }

  fs.writeFileSync(OUTPUT_FILE, content, 'utf8');
  console.log(`Successfully generated products list at: ${OUTPUT_FILE}`);

  // Generate sitemap.xml and robots.txt
  const DOMAIN = 'https://techlabscientific.com';
  const currentDate = new Date().toISOString().split('T')[0];

  // Core static pages
  const staticPages = [
    { path: '', changefreq: 'monthly', priority: '1.0' },
    { path: '/about', changefreq: 'monthly', priority: '0.8' },
    { path: '/services', changefreq: 'monthly', priority: '0.8' },
    { path: '/products', changefreq: 'weekly', priority: '0.8' },
    { path: '/contact', changefreq: 'monthly', priority: '0.8' },
  ];

  // Static product IDs ("01" to "17")
  const staticProductIds = Array.from({ length: 17 }, (_, i) => String(i + 1).padStart(2, '0'));
  
  // Dynamic product IDs
  const dynamicProductIds = products.map(p => p.id);

  const allProductIds = [...staticProductIds, ...dynamicProductIds];

  let sitemapContent = `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
`;

  // Append static pages
  for (const page of staticPages) {
    sitemapContent += `  <url>
    <loc>${DOMAIN}${page.path}</loc>
    <lastmod>${currentDate}</lastmod>
    <changefreq>${page.changefreq}</changefreq>
    <priority>${page.priority}</priority>
  </url>
`;
  }

  // Append product pages
  for (const productId of allProductIds) {
    sitemapContent += `  <url>
    <loc>${DOMAIN}/product/${productId}</loc>
    <lastmod>${currentDate}</lastmod>
    <changefreq>weekly</changefreq>
    <priority>0.7</priority>
  </url>
`;
  }

  sitemapContent += `</urlset>
`;

  const sitemapPath = path.join(__dirname, '..', 'public', 'sitemap.xml');
  fs.writeFileSync(sitemapPath, sitemapContent, 'utf8');
  console.log(`Successfully generated sitemap at: ${sitemapPath}`);

  // Generate robots.txt
  const robotsContent = `User-agent: *
Allow: /

# Sitemap
Sitemap: ${DOMAIN}/sitemap.xml
`;

  const robotsPath = path.join(__dirname, '..', 'public', 'robots.txt');
  fs.writeFileSync(robotsPath, robotsContent, 'utf8');
  console.log(`Successfully generated robots.txt at: ${robotsPath}`);
}

scanDirectory();
